#include <iostream>
#include <random>

// Класс одномерного фильтра Калмана
class KalmanFilter {
    double x, P, Q, R, F = 1, H = 1;  // Переменные состояния фильтра
public:
    // Конструктор: инициализация параметров фильтра
    KalmanFilter(double init, double cov, double p_noise, double m_noise)
        : x(init), P(cov), Q(p_noise), R(m_noise) {
    }

    // Шаг прогноза: предсказание состояния на основе модели
    void predict() {
        x = F * x;           // Прогноз следующего состояния
        P = F * P * F + Q;   // Обновление ковариации с учетом шума процесса
    }

    // Шаг коррекции: обновление оценки на основе измерения
    void update(double z) {
        double y = z - H * x;        // Невязка (разность между измерением и прогнозом)
        double S = H * P * H + R;    // Ковариация невязки
        double K = (P * H) / S;      // Коэффициент усиления Калмана
        x = x + K * y;               // Коррекция оценки состояния
        P = (1 - K * H) * P;         // Коррекция ковариации ошибки
    }

    // Получение текущей оценки состояния
    double getState() { return x; }
};

int main() {
    setlocale(LC_ALL, "RU");  // Включение русской локали для корректного вывода

    // Создание фильтра Калмана с параметрами:
    KalmanFilter kf(0, 1, 0.01, 0.1);  // начальное состояние=0, ковариация=1, 
    // шум процесса=0.01, шум измерений=0.1

// Настройка генератора случайных чисел для имитации шума измерений
    std::random_device rd;
    std::mt19937 gen(rd());
    std::normal_distribution<> noise(0, 0.3);  // Нормальное распределение шума

    double true_value = 10.0;  // Истинное значение (в реальной системе неизвестно)

    // Заголовок таблицы результатов
    std::cout << "Шаг\tИстинное\tИзмерение\tОценка\n";

    // Основной цикл работы фильтра (10 итераций)
    for (int i = 0; i < 10; i++) {
        true_value += 0.1;                    // Истинное значение медленно растет
        double measured = true_value + noise(gen);  // Зашумленное измерение

        kf.predict();        // Этап прогноза фильтра
        kf.update(measured); // Этап коррекции по измерению

        // Вывод результатов текущей итерации
        printf("%d\t%.2f\t\t%.2f\t\t%.2f\n",
            i, true_value, measured, kf.getState());
    }
    return 0;
}